    public void Restar(){
        int contC=Integer.parseInt(CupoCarros.getText());
        int contM=Integer.parseInt(CupoMotos.getText());
    String opc = (String)tipos.getSelectedItem();
        
        if (opc.equals("moto")) {
            contM=contM-1;
            CupoMotos.setText(""+contM);
        } else {
            contC=contC-1;
            CupoCarros.setText(""+contC);
        }
        
    }
    public void Resultado(){
    String matris [] []= new String [Lista.size()][5];
    
        for (int i = 0; i < Lista.size(); i++) {
            
           matris [i] [0]= Lista.get(i).getNumPlaca();
           matris [i] [1]= Lista.get(i).getTipo();
           matris [i] [2]= Lista.get(i).getHEntrada();
           matris [i] [3]= Lista.get(i).getHSalida();
           matris [i] [4]= Lista.get(i).getNombre();
            
        }
    tabla.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "#Placa", "Nombre", "Hora Entrada", "Tipo", "Hora salida"
            }
        ));
    }
