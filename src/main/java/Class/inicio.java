
package Class;


public class inicio {

    String ValorM;
    String ValorC;
    String CuposC;
    String CuposM;


    public inicio(String ValorM, String ValorC, String CuposC, String CuposM) {
        this.ValorM = ValorM;
        this.ValorC = ValorC;
        this.CuposC = CuposC;
        this.CuposM = CuposM;
    }

    public String getValorM() {
        return ValorM;
    }

    public void setValorM(String ValorM) {
        this.ValorM = ValorM;
    }

    public String getValorC() {
        return ValorC;
    }

    public void setValorC(String ValorC) {
        this.ValorC = ValorC;
    }

    public String getCuposC() {
        return CuposC;
    }

    public void setCuposC(String CuposC) {
        this.CuposC = CuposC;
    }

    public String getCuposM() {
        return CuposM;
    }

    public void setCuposM(String CuposM) {
        this.CuposM = CuposM;
    }
}
